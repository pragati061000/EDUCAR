var qnaButton = document.querySelector(".qna");
var open = false;
qnaButton.onclick = function toggleQna() {
  if (!open) {
    var qnaDiv = document.createElement("div");
    qnaDiv.setAttribute("id", "qnaDiv");
    var qnaTextArea = document.createElement("textarea");
    qnaTextArea.setAttribute("class", "qnaTextArea");

    var qnaSubmit = document.createElement("button");
    qnaSubmit.setAttribute("id", "qnaSubBtn");
    qnaSubmit.setAttribute("class", "btn btn-warning");
    qnaSubmit.innerText = "Submit";

    qnaDiv.append(qnaTextArea, qnaSubmit);

    var contentDiv = document.querySelector(".template-content-content");
    contentDiv.appendChild(qnaDiv);
    open = true;
  } else {
    var element = document.querySelector("#qnaDiv");
    element.parentNode.removeChild(element);
    open = false;
  }
};
